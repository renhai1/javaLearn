public class calculate {
    public static void main(String[] args) {
        int num1 = 50;
        int num2 = 25;
        //加法
        System.out.println(num1 + num2);
        //减法
        System.out.println(num1 - num2);
        //乘法
        System.out.println(num1 * num2);
        //除法
        System.out.println(num1 / num2);
        //取余
        System.out.println(num1 % num2);
    }
}
